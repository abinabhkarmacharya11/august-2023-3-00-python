# Strings are the immutable datatypes in python
# They are also sequential types like list. So, indexing and slicing are possible in the string


# Creating an empty string
a= str() #empty string
a = "" # empty string

# Creating a non-empty strings
message = "Hello world" # using double-quotes
message = 'hello world' #using single quote
message = """hello world """ # using triple-quotes






